Model::Model(ModelType model, glm::vec3 pos) : pos(pos)
{
	switch(model)
	{
		case CUBE:
			{
				vertices=cube;
				verticesSize=cubeSize;
				cubesNumber++;
				cubesList.push_back(*this);
				break;
			}
		default: break;
	}
}

float* Model::GetVertices(void)
{
	return vertices;
}

float Model::GetVerticesSize(void)
{
	return verticesSize;
}

glm::vec3 Model::GetPos(void)
{
	return pos;
}

